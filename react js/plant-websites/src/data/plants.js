export const getPlantById = (id) =>
  plants.find((p) => p.id === Number(id));

export const getRelatedPlants = (plant, limit = 3) =>
  plants
    .filter((p) => p.id !== plant.id && p.category === plant.category)
    .slice(0, limit);

export const getCategories = () => [
  ...new Set(plants.map((p) => p.category)),
];
